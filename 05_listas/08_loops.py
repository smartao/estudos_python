### For loop ###
print("Exemplo basico de for loop, onde os elementos apareceram com maiuscula")
animals = ['man', 'bear', 'pig']
for animal in animals:
    print(animal.upper())

### While Loop ###

print("\nExemplo basico de while loop")
animals = ['man', 'bear', 'pig', 'cow', 'duck', 'horse', 'dog']
index = 0
while index < len(animals):
    print(animals[index])
    index += 1



